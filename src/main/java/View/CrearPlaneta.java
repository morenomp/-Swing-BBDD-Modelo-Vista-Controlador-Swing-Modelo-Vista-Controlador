    /**
     * Botom para crear planeta
     *
     * @param evt
     */
    private void crearplanetaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_crearplanetaActionPerformed
        // TODO add your handling code here:
        //Variables de las propiedades de planeta
        String nameplanet = nombreplaneta.getText();
        String namegalaxy = nombregalaxia.getText();
        int maxpopulation = (int) poblacionmaxima.getValue();
        String weather = (String) clima.getSelectedItem();
        boolean floraa = flora.isSelected();
        boolean aquaticc = aquatic.isSelected();

        if (!(nameplanet.isEmpty() || namegalaxy.isEmpty())) {
            if (nameplanet.length() <= 3 && namegalaxy.length() <= 3) {
                JOptionPane.showMessageDialog(this, "La longitud del nombre de la galaxia y nombre"
                    + "del planeta no son  correctos (4)",
                    "Nombres incorrectos", JOptionPane.WARNING_MESSAGE);
            } else if (nameplanet.length() <= 3) {
                JOptionPane.showMessageDialog(this, "La longitud del nombre del planeta no es correcto (4)",
                    "Nombre planeta incorrecto", JOptionPane.WARNING_MESSAGE);
            } else if (namegalaxy.length() <= 3) {
                JOptionPane.showMessageDialog(this, "La longitud del nombre de la galaxia no es correcto (4)",
                    "Nombre galaxia incorrecto", JOptionPane.WARNING_MESSAGE);
            } else if (maxpopulation < 0) {
                JOptionPane.showMessageDialog(this, "No se puede introducir numeros negativos",
                    "Numeros negativos", JOptionPane.WARNING_MESSAGE);
            } else {
                Planeta p = new Planeta(nameplanet, namegalaxy, maxpopulation, weather, floraa, aquaticc);
                try {
                    createplanet(p);
                    JOptionPane.showMessageDialog(this, "Se ha registrado correctamente el planeta",
                        "Planeta Registrado", JOptionPane.INFORMATION_MESSAGE);
                    dispose();
                } catch (PlanetaExcepcion ex) {
                    nombreplaneta.setText("");
                    JOptionPane.showMessageDialog(this, ex.getMessage(),
                        "Planeta No Registrado", JOptionPane.ERROR_MESSAGE);
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "No se puede dejar campos en blanco", "Datos vacÃ­os", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_crearplanetaActionPerformed

